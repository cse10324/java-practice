package babystep;

public class FloatAndDouble {

  public static void main(String[] args) {
    
    float myFloatMinValue = Float.MIN_VALUE;
    float myFloatMaxValue = Float.MAX_VALUE;
    
    System.out.println("My Minimum Float Value: " +myFloatMinValue);
    System.out.println("My Maximum Float Value: " +myFloatMaxValue);
    
    double myDoubleMinValue = Double.MIN_VALUE;
    double myDoubleMaxValue = Double.MAX_VALUE;
    
    System.out.println("My Minimum Double Value: " +myDoubleMinValue);
    System.out.println("My Maximum Double Value: " +myDoubleMaxValue);
    
    int myIntValue = 5;
    float myFloatValue =  5.25f;
    double myDoubleValue = 5.25d;
    
    
    
  }

}
