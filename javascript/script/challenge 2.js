function changeColor(){
  var firstDiv = document.getElementById("div1");
  var secondDiv = document.getElementById("div2");
  
  firstDiv.className="hotpink";
  secondDiv.className="lightblue";
}

function changeText(){
  var firstDiv = document.getElementById("div1");
  var secondDiv = document.getElementById("div2");
  
  firstDiv.innerHTML="hotpink";
  secondDiv.innerHTML="lightblue";
}